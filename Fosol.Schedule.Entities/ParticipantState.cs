namespace Fosol.Schedule.Entities
{
    /// <summary>
    /// ParticipantState enum, provides the participant state options.
    /// </summary>
    public enum ParticipantState
    {
        /// <summary>
        /// This participant is disabled.
        /// </summary>
        Disabled = 0,

        /// <summary>
        /// This participant is enabled.
        /// </summary>
        Enabled = 1,

        /// <summary>
        /// This participant is locked.
        /// </summary>
        Locked = 2
    }
}